    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed
        // TODO add your handling code here:

        
        if (textVlRelogioMesPassado.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Numero Obrigatório");
            textVlRelogioMesPassado.requestFocus();
            return;
        }
        BigDecimal vlRelogioMesPassado = new BigDecimal(textVlRelogioMesPassado.getText());
        BigDecimal vlRelogioMesAtual = new BigDecimal(textVlRelogioMesAtual.getText());
        BigDecimal vlTarifaMes = new BigDecimal(textVlTarifaMes.getText());
        
         BigDecimal vlTotal = vlRelogioMesAtual.subtract(vlRelogioMesPassado).multiply(vlTarifaMes);
                
        textVlTotal.setText(vlTotal.toString());
        try{
            if (textId.getText().isEmpty()){//novo
                dao.add(createObject());
            }else{//atualizacao
                dao.update(createObject());
            }
            this.iniciaComponentes();
            this.carregaTable();
        }catch(ParseException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

