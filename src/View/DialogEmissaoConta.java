    private EmissaoConta createObject() throws ParseException{
        return (new EmissaoConta(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()),
                (Pessoa)comboPessoaCliente.getSelectedItem(),
                (Pessoa)comboPessoaFuncionario.getSelectedItem(),
                new BigDecimal(textVlRelogioMesPassado.getText()),
                new BigDecimal(textVlRelogioMesAtual.getText()),
                formato.parse(textDtAfericao.getText()),
                new BigDecimal(textVlTarifaMes.getText()),
                new BigDecimal(textVlTotal.getText())
        ));
    }

