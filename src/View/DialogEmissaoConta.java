        private void fillComponents(EmissaoConta emissaoConta){
            textId.setText(emissaoConta.getId()+"");
            textVlRelogioMesPassado.setText(emissaoConta.getVlRelogioMesPassado().toString());
            textVlRelogioMesAtual.setText(emissaoConta.getVlRelogioMesAtual().toString());
            textVlTarifaMes.setText(emissaoConta.getVlTarifaMes().setScale(2, RoundingMode.DOWN).toString());
            textVlTotal.setText(emissaoConta.getVlTotal().setScale(2, RoundingMode.DOWN).toString());
            textDtAfericao.setText(formato.format(emissaoConta.getDtAfericao()));
            comboPessoaCliente.setSelectedItem(emissaoConta.getCliente());
            comboPessoaFuncionario.setSelectedItem(emissaoConta.getFuncionario());

        } 
